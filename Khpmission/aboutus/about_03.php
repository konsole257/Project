                        <div>
                            <p><strong>제19조 본회의 기구와 업무</strong></p>
                            <div>
                                <p>본회는 산하(傘下)에 「선교부」 「교육부」 「출판부」 「사무국」 「본부 자체교회(성민중앙교회)」를 둔다.</p>
                                <p>
                                    1. 선교부(宣敎部) : 국내외(國內外)에 「완전성 진리」를 문서(文書)나 영상물(映像物)로 반포한다.<br>
                                    2. 교육부(敎育部)<br>
                                     ① 「성민성서학습원(聖民聖書學習院)」에서 전도자의 양성을 위하여 진리를 가르치고 수련을 받게 한다.<br>
                                     ② 회원들에게 수시로 진리를 강론하여 믿음의 성장을 이루게 한다.<br>
                                     ③ 반포된 진리를 깨닫고 그 진리를 더 알기를 원하는 자들에게 특별 강론을 한다.<br>
                                    3. 출판부(出版部) : 「성민복음사(聖民福音社)」에서 문서선교(文書宣敎)에 필요한 각종(各種) 출판물을 간행(刊行)한다. 이 모든 자료는 비매품(非賣品)으로 한다.<br>
                                    4. 사무국(事務局) : 각부의 사무 전반을 담당한다.<br>
                                    5. 본부 자체교회(성민중앙교회) :「완전성 진리」를 깨달은 선교회 회원들이나 그 진리를 깨달은 자들이 자원하여 예배(禮拜)를 드린다. 또한 교회의 운영은 별도 규정에 의한다.
                                </p>
