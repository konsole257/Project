                                </p>
                                <p>
                                    위와 같이 행하게 되는 모든 경비(經費)는 자체교회나 진리를 깨달아 인류구원을 위하여 자원하여 바치게 되는 헌금에 의한다(마 26:6-13). 따라서 그러한 행사는 성금(誠金)이 허락되는 한도(限度) 안에서 이루어지게 된다.
                                </p>
