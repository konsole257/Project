                            <div>
                                <p>
                                    <span>ㆍ</span>기본개념<br>
                                    이 규약(規約)을 정하는 목적은 하나님의 경륜(經綸)안에서(엡 3:9) 마지막 날을 맞이하여 만세(萬世)와 만대(萬代)로부터 옴으로 감취어져(골 1:26) 오늘날까지 들어보지 못한(사 48:7)「완전성 진리」를(시 18:30) 하나님께서 온 세계 중 이 나라 이 땅에(단 2:44) 특별히 택함받은 사자(使者)에게 계시해 주셨기 때문에(마 11:27) 이 천국복음을(마 24:14) 천하만민(天下萬民)에게 교파를 초월(超越)하여 전파함으로 환난 당하는 인류를 완전성 진리로 구원하게 된다는 것이다.
                                </p>
                                <p>
                                    <span>ㆍ</span>하나님 자신의 신성으로 구원<br>
                                    이 마지막 날에는 사망의 영이(요I 2:18) 작용하는 최후의 대환난이기 때문에 인간을 지으신 창조주 자신께서 스스로의 진리를 친히 인간들에게 나타내 주시지 아니하고는 결코 인류를 극악(極惡)의 환난에서 건져낼 수가 없는 것이다. 대환난에 처한 인간들이 세상의 어떠한 방법으로도 구원을 받을 수가 없으므로 세상의 방법을 포기(抛棄)하고 마지막으로 오직 어디엔가 절대자의 구원을 갈구하게 되는 것이다(시 121:1-2).
                                    그렇게 되었을 때 장구(長久)한 세월 동안 세상 방법에만 의지하여 어려움을 해결해 오던 인간들이 자유의지를 절대자 쪽으로 기울여 마지막 구원을 간구(懇求)하게 되었기 때문에 성신께서는 이제 그 자유의지에 연결이 될 수가 있게 된 까닭에 오랜 세월 고대(苦待)했던 심령으로(사 30:18) 그 기울어진 자유의지에 완전성 진리를 깨닫게 해 줌으로 구원해 주실 수가 있게 되었다는 것이다.
                                </p>
                                <p>
                                    <span>ㆍ</span>마지막 복음 한국 땅에 계시됨<br>
                                    그러므로 하나님께서는 친히 만세 전부터 택하신 사자에게(렘 1:5) 스스로의 구속의 도리인 천국복음을 「아들의 소원대로」(마 11:27) 계시해 주셔서 환난 당하는 인류를 구원하시고자 하셨는 바, 그 진리가 마침 이 한국 사람에게 계시가 되었으므로(사 41:2) 그 완전성 진리를 이 대한민국 땅에서(시 33:12) 천하만민에게 전파하게 되었다는 것이다.
                                </p>
                                <p>
                                    <span>ㆍ</span>창조주께서 완전무상으로 인류구원을 이룩하심<br>
                                    따라서 창조주께서는 천부의 사랑으로 대환난 중에 있는 천하만민을 구원하시는 것이기 때문에 극심한 고통 중에 있는 인간들에게 돈을 내야만 구원해 주신다고 할 리가 없는 것이다.
                                    그러므로 이 마지막 날의 인류구원의 역사(役事)는 성경에「값없이 팔렸으니 돈없이 속량(贖良)되리라」(사 52:3) 고 계시하심과 같이 완전무상(完全無償)으로 이루어지게 된다는 것을 깨달아야 하는 것이다.
                                    따라서 이러한 인류구원의 역사는 성신께서 주시는 진리에 의하여 극한적 환난에서 구원받게 된 자들이 천부의 사랑을 깨닫고 그 하나님께 사랑의 빚을(롬 1:14) 갚는다는 마음으로 바쳐지는 성금(誠金)에 의하여 하나님의 뜻대로 이루어지게 된다는 것이다.
                                </p>
