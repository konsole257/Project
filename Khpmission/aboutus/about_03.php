                                    2. 필요에 따라 자원 봉사자를 수시로 수련(修練)한다.<br>
                                    3. 본「성민성서학습원」은 일반 신학교(神學校)와 같이 수학(修學) 연한이 한정(限定)되어 있는 것이 아니라 오직 진리 안에서 지혜(智慧)와 능력(能力)이 나타나(고전 2:4) 성신의 인도하심이 있을 때까지 학습과 수련을 받게 된다.
                                </p>
