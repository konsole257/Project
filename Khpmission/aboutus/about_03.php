                                    2. 자격(資格) : 누구든지 진리를 깨닫고 성령을 받은 자로서 믿음의 과정에서 연단되어(롬 5:4) 그리스도의 지혜와 능력을 나타낼 수 있는 자(행 8:17).<br>
                                    3. 임명(任命) : 위의 자격을 갖추었다고 인정(認定)되는 자로서 이사회의 천거(薦擧)를 받아 회장이 주의 이름으로 임명한다.
                                </p>
